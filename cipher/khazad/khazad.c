/* KHAZAD data interpretation
 *
 * 0    1    2    3    4    5    6    7    8
 * +----+----+----+----+----+----+----+----+
 * | P0 | P1 | P2 | P3 | P4 | P5 | P6 | P7 |  ->  0xP0P1P2P3P4P5P6P7
 * +----+----+----+----+----+----+----+----+
 *
 * 0    1    2    3    4    5    6    7    8
 * +----+----+----+----+----+----+----+----+
 * | C0 | C1 | C2 | C3 | C4 | C5 | C6 | C7 |  ->  0xC0C1C2C3C4C5C6C7
 * +----+----+----+----+----+----+----+----+
 *
 * 0    1             15   16                  128-bit key
 * +----+---- ... ----+----+
 * | K0 | K1  ...  KE | KF |  ->  0xK0K1K2K3K4K5K6K7 0xK8K9KAKBKCKDKEKF
 * +----+---- ... ----+----+              L                  R
 */

#include "khazad.h"
#include <string.h>
#include "config.h"

typedef blkcphr_u8_t  khazad_byte_t;
typedef blkcphr_u64_t khazad_word_t;

typedef khazad_byte_t khazad_brick_t[8];

static const khazad_byte_t khazadi_gf_by_3[256];
static const khazad_byte_t khazadi_gf_by_4[256];
static const khazad_byte_t khazadi_gf_by_5[256];
static const khazad_byte_t khazadi_gf_by_6[256];
static const khazad_byte_t khazadi_gf_by_7[256];
static const khazad_byte_t khazadi_gf_by_8[256];
static const khazad_byte_t khazadi_gf_by_b[256];

static const khazad_byte_t khazadi_S[256];
static const khazad_word_t khazadi_C[9];

static struct {
    khazad_word_t K[9];
} khazadi_ctx;

#define GFBY1(x)                (x)
#define GFBY3(x) khazadi_gf_by_3[x]
#define GFBY4(x) khazadi_gf_by_4[x]
#define GFBY5(x) khazadi_gf_by_5[x]
#define GFBY6(x) khazadi_gf_by_6[x]
#define GFBY7(x) khazadi_gf_by_7[x]
#define GFBY8(x) khazadi_gf_by_8[x]
#define GFBYb(x) khazadi_gf_by_b[x]

static void khazadi_write_to_brick(khazad_brick_t out, khazad_word_t in) {
    out[7] = (in >> 56) & 0xFF;
    out[6] = (in >> 48) & 0xFF;
    out[5] = (in >> 40) & 0xFF;
    out[4] = (in >> 32) & 0xFF;
    out[3] = (in >> 24) & 0xFF;
    out[2] = (in >> 16) & 0xFF;
    out[1] = (in >>  8) & 0xFF;
    out[0] = (in >>  0) & 0xFF;
}

static khazad_word_t khazadi_read_from_brick(khazad_brick_t in) {
    khazad_word_t out = 0;
    out |= (khazad_word_t)in[7] << 56;
    out |= (khazad_word_t)in[6] << 48;
    out |= (khazad_word_t)in[5] << 40;
    out |= (khazad_word_t)in[4] << 32;
    out |= (khazad_word_t)in[3] << 24;
    out |= (khazad_word_t)in[2] << 16;
    out |= (khazad_word_t)in[1] <<  8;
    out |= (khazad_word_t)in[0] <<  0;
    return out;
}

static khazad_word_t khazadi_lintrans(khazad_word_t in) {
    khazad_brick_t T, S;
    khazadi_write_to_brick(S, in);

    T[0] = GFBY1(S[0]) ^ GFBY3(S[1]) ^ GFBY4(S[2]) ^ GFBY5(S[3]) ^ GFBY6(S[4]) ^ GFBY8(S[5]) ^ GFBYb(S[6]) ^ GFBY7(S[7]);
    T[1] = GFBY3(S[0]) ^ GFBY1(S[1]) ^ GFBY5(S[2]) ^ GFBY4(S[3]) ^ GFBY8(S[4]) ^ GFBY6(S[5]) ^ GFBY7(S[6]) ^ GFBYb(S[7]);
    T[2] = GFBY4(S[0]) ^ GFBY5(S[1]) ^ GFBY1(S[2]) ^ GFBY3(S[3]) ^ GFBYb(S[4]) ^ GFBY7(S[5]) ^ GFBY6(S[6]) ^ GFBY8(S[7]);
    T[3] = GFBY5(S[0]) ^ GFBY4(S[1]) ^ GFBY3(S[2]) ^ GFBY1(S[3]) ^ GFBY7(S[4]) ^ GFBYb(S[5]) ^ GFBY8(S[6]) ^ GFBY6(S[7]);
    T[4] = GFBY6(S[0]) ^ GFBY8(S[1]) ^ GFBYb(S[2]) ^ GFBY7(S[3]) ^ GFBY1(S[4]) ^ GFBY3(S[5]) ^ GFBY4(S[6]) ^ GFBY5(S[7]);
    T[5] = GFBY8(S[0]) ^ GFBY6(S[1]) ^ GFBY7(S[2]) ^ GFBYb(S[3]) ^ GFBY3(S[4]) ^ GFBY1(S[5]) ^ GFBY5(S[6]) ^ GFBY4(S[7]);
    T[6] = GFBYb(S[0]) ^ GFBY7(S[1]) ^ GFBY6(S[2]) ^ GFBY8(S[3]) ^ GFBY4(S[4]) ^ GFBY5(S[5]) ^ GFBY1(S[6]) ^ GFBY3(S[7]);
    T[7] = GFBY7(S[0]) ^ GFBYb(S[1]) ^ GFBY8(S[2]) ^ GFBY6(S[3]) ^ GFBY5(S[4]) ^ GFBY4(S[5]) ^ GFBY3(S[6]) ^ GFBY1(S[7]);

    return khazadi_read_from_brick(T);
}

static khazad_word_t khazadi_full_round(khazad_word_t in, khazad_word_t key) {
    khazad_brick_t S, T;
    khazadi_write_to_brick(S, in);

    S[0] = khazadi_S[S[0]]; S[1] = khazadi_S[S[1]];
    S[2] = khazadi_S[S[2]]; S[3] = khazadi_S[S[3]];
    S[4] = khazadi_S[S[4]]; S[5] = khazadi_S[S[5]];
    S[6] = khazadi_S[S[6]]; S[7] = khazadi_S[S[7]];

    T[0] = GFBY1(S[0]) ^ GFBY3(S[1]) ^ GFBY4(S[2]) ^ GFBY5(S[3]) ^ GFBY6(S[4]) ^ GFBY8(S[5]) ^ GFBYb(S[6]) ^ GFBY7(S[7]);
    T[1] = GFBY3(S[0]) ^ GFBY1(S[1]) ^ GFBY5(S[2]) ^ GFBY4(S[3]) ^ GFBY8(S[4]) ^ GFBY6(S[5]) ^ GFBY7(S[6]) ^ GFBYb(S[7]);
    T[2] = GFBY4(S[0]) ^ GFBY5(S[1]) ^ GFBY1(S[2]) ^ GFBY3(S[3]) ^ GFBYb(S[4]) ^ GFBY7(S[5]) ^ GFBY6(S[6]) ^ GFBY8(S[7]);
    T[3] = GFBY5(S[0]) ^ GFBY4(S[1]) ^ GFBY3(S[2]) ^ GFBY1(S[3]) ^ GFBY7(S[4]) ^ GFBYb(S[5]) ^ GFBY8(S[6]) ^ GFBY6(S[7]);
    T[4] = GFBY6(S[0]) ^ GFBY8(S[1]) ^ GFBYb(S[2]) ^ GFBY7(S[3]) ^ GFBY1(S[4]) ^ GFBY3(S[5]) ^ GFBY4(S[6]) ^ GFBY5(S[7]);
    T[5] = GFBY8(S[0]) ^ GFBY6(S[1]) ^ GFBY7(S[2]) ^ GFBYb(S[3]) ^ GFBY3(S[4]) ^ GFBY1(S[5]) ^ GFBY5(S[6]) ^ GFBY4(S[7]);
    T[6] = GFBYb(S[0]) ^ GFBY7(S[1]) ^ GFBY6(S[2]) ^ GFBY8(S[3]) ^ GFBY4(S[4]) ^ GFBY5(S[5]) ^ GFBY1(S[6]) ^ GFBY3(S[7]);
    T[7] = GFBY7(S[0]) ^ GFBYb(S[1]) ^ GFBY8(S[2]) ^ GFBY6(S[3]) ^ GFBY5(S[4]) ^ GFBY4(S[5]) ^ GFBY3(S[6]) ^ GFBY1(S[7]);

    return khazadi_read_from_brick(T) ^ key;
}

static khazad_word_t khazadi_part_round(khazad_word_t in, khazad_word_t key) {
    khazad_brick_t S;
    khazadi_write_to_brick(S, in);

    S[0] = khazadi_S[S[0]]; S[1] = khazadi_S[S[1]];
    S[2] = khazadi_S[S[2]]; S[3] = khazadi_S[S[3]];
    S[4] = khazadi_S[S[4]]; S[5] = khazadi_S[S[5]];
    S[6] = khazadi_S[S[6]]; S[7] = khazadi_S[S[7]];

    return khazadi_read_from_brick(S) ^ key;
}

int khazad_init(const void* key, int b) {
    khazad_word_t L, R, T; int i;

    memcpy(&L, (const char*)key + 0, 8);
    memcpy(&R, (const char*)key + 8, 8);
#if BLKCPHR_IS_LITTLE
    L = blkcphr_bswap64(L);
    R = blkcphr_bswap64(R);
#endif

    for (i = 0; i < 9; i++) {
        L ^= khazadi_full_round(R, khazadi_C[i]);
        T = L; L = R; R = T;
        khazadi_ctx.K[i] = R;
    }

    return 0; (void)b;
}

static khazad_word_t khazadi_write_to_block(const void* src) {
    khazad_word_t block;
    memcpy(&block, src, KHAZAD_BLOCK_BYTE);
#if BLKCPHR_IS_LITTLE
    block = blkcphr_bswap64(block);
#endif
    return block;
}

static void khazadi_read_from_block(void* dst, khazad_word_t block) {
#if BLKCPHR_IS_LITTLE
    block = blkcphr_bswap64(block);
#endif
    memcpy(dst, &block, KHAZAD_BLOCK_BYTE);
}

void khazad_block_encode(void* dst, const void* src) {
    khazad_word_t block; int i;
    block = khazadi_write_to_block(src);

    block ^= khazadi_ctx.K[0];
    for (i = 1; i < 8; i++)
        block = khazadi_full_round(block, khazadi_ctx.K[i]);
    block = khazadi_part_round(block, khazadi_ctx.K[8]);

    khazadi_read_from_block(dst, block);
}

void khazad_block_decode(void* dst, const void* src) {
    khazad_word_t block; int i;
    block = khazadi_write_to_block(src);

    block ^= khazadi_ctx.K[8];
    for (i = 8; i --> 1;)
        block = khazadi_full_round(block, khazadi_lintrans(khazadi_ctx.K[i]));
    block = khazadi_part_round(block, khazadi_ctx.K[0]);

    khazadi_read_from_block(dst, block);
}

