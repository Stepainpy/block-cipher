#include "kuznyechik.h"
#include <string.h>
#include "config.h"

typedef blkcphr_u8_t kuzya_byte_t;

typedef kuzya_byte_t kuzya_block_t[16];

static const kuzya_byte_t kuzyai_S_fwd[256];
static const kuzya_byte_t kuzyai_S_inv[256];
static const kuzya_byte_t kuzyai_gf_by[16][256];
static const kuzya_block_t kuzyai_C[32];

static struct {
    kuzya_block_t K[10];
} kuzyai_ctx;

static void kuzyai_addkey(kuzya_block_t block, const kuzya_block_t key) {
    const blkcphr_u64_t* K = (const void*)key;
    blkcphr_u64_t* B = (void*)block;
    B[0] ^= K[0]; B[1] ^= K[1];
}

static void kuzyai_subst_fwd(kuzya_block_t block) {
    int i; for (i = 0; i < KUZNYECHIK_BLOCK_BYTE; i++)
        block[i] = kuzyai_S_fwd[block[i]];
}

static void kuzyai_subst_inv(kuzya_block_t block) {
    int i; for (i = 0; i < KUZNYECHIK_BLOCK_BYTE; i++)
        block[i] = kuzyai_S_inv[block[i]];
}

static void kuzyai_shift_fwd(kuzya_block_t block) {
    int i, j;
    for (j = 0; j < 16; j++) {
        kuzya_byte_t sum = 0;
        for (i = 0; i < 16; i++) {
            sum ^= kuzyai_gf_by[i][block[i]];
            if (i) block[i - 1] = block[i];
        }
        block[15] = sum;
    }
}

static void kuzyai_shift_inv(kuzya_block_t block) {
    int i, j;
    for (j = 0; j < 16; j++) {
        kuzya_byte_t MSB = block[15];
        kuzya_byte_t sum = 0;
        for (i = 16; i --> 0;) {
            sum ^= kuzyai_gf_by[i][i ? block[i - 1] : MSB];
            block[i] = i ? block[i - 1] : sum;
        }
    }
}

static void kuzyai_write_to_block(kuzya_block_t out, const void* src) {
    const blkcphr_u64_t* S = src;
    blkcphr_u64_t* D = (void*)out;
    D[0] = blkcphr_bswap64(S[1]);
    D[1] = blkcphr_bswap64(S[0]);
}

static void kuzyai_read_from_block(void* dst, const kuzya_block_t in) {
    const blkcphr_u64_t* S = (const void*)in;
    blkcphr_u64_t* D = dst;
    D[0] = blkcphr_bswap64(S[1]);
    D[1] = blkcphr_bswap64(S[0]);
}

int kuznyechik_init(const void* key, int bits) {
    kuzya_block_t L, R, T; int i, j;
    if (bits != 256) return 1;

    kuzyai_write_to_block(L, (const char*)key +  0);
    kuzyai_write_to_block(R, (const char*)key + 16);
    memcpy(kuzyai_ctx.K[0], L, KUZNYECHIK_BLOCK_BYTE);
    memcpy(kuzyai_ctx.K[1], R, KUZNYECHIK_BLOCK_BYTE);

    for (i = 0; i < 4; i++) {
        for (j = 0; j < 8; j++) {
            memcpy(T, L, KUZNYECHIK_BLOCK_BYTE);
            kuzyai_addkey(T, kuzyai_C[8 * i + j]);
            kuzyai_subst_fwd(T);
            kuzyai_shift_fwd(T);
            kuzyai_addkey(T, R);
            memcpy(R, L, KUZNYECHIK_BLOCK_BYTE);
            memcpy(L, T, KUZNYECHIK_BLOCK_BYTE);
        }

        memcpy(kuzyai_ctx.K[2 * (i + 1) + 0], L, KUZNYECHIK_BLOCK_BYTE);
        memcpy(kuzyai_ctx.K[2 * (i + 1) + 1], R, KUZNYECHIK_BLOCK_BYTE);
    }

    return 0;
}

void kuznyechik_block_encode(void* dst, const void* src) {
    kuzya_block_t block; int i;
    kuzyai_write_to_block(block, src);

    for (i = 0; i < 9; i++) {
        kuzyai_addkey(block, kuzyai_ctx.K[i]);
        kuzyai_subst_fwd(block);
        kuzyai_shift_fwd(block);
    }
    kuzyai_addkey(block, kuzyai_ctx.K[9]);

    kuzyai_read_from_block(dst, block);
}

void kuznyechik_block_decode(void* dst, const void* src) {
    kuzya_block_t block; int i;
    kuzyai_write_to_block(block, src);

    for (i = 10; i --> 1;) {
        kuzyai_addkey(block, kuzyai_ctx.K[i]);
        kuzyai_shift_inv(block);
        kuzyai_subst_inv(block);
    }
    kuzyai_addkey(block, kuzyai_ctx.K[0]);

    kuzyai_read_from_block(dst, block);
}

