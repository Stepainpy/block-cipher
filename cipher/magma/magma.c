/* Magma data interpretation
 *
 * 0    1    2    3    4    5    6    7    8
 * +----+----+----+----+----+----+----+----+
 * | P0 | P1 | P2 | P3 | P4 | P5 | P6 | P7 |  ->  0xP0P1P2P3 0xP4P5P6P7
 * +----+----+----+----+----+----+----+----+          L          R
 * ^- src
 *
 * 0    1    2    3    4    5    6    7    8
 * +----+----+----+----+----+----+----+----+
 * | C0 | C1 | C2 | C3 | C4 | C5 | C6 | C7 |  ->  0xC0C1C2C3 0xC4C5C6C7
 * +----+----+----+----+----+----+----+----+          L          R
 * ^- dest
 *
 * 0    1             15   16   17            31   32                 256-bit key
 * +----+---- ... ----+----+----+---- ... ----+----+
 * | K0 | K1  ...  KE | KF | k0 | k1  ...  kE | kF |  ->  K0K1K2K3 K4K5K6K7 K8K9KAKB KCKDKEKF
 * +----+---- ... ----+----+----+---- ... ----+----+         [0]      [1]      [2]      [3]
 * ^- key                                                 k0k1k2k3 k4k5k6k7 k8k9kAkB kCkDkEkF
 *                                                           [4]      [5]      [6]      [7]
 */

#include "magma.h"
#include <string.h>
#include "config.h"

typedef blkcphr_u8_t  magma_byte_t;
typedef blkcphr_u32_t magma_word_t;

static const magma_byte_t magmai_S01[256];
static const magma_byte_t magmai_S23[256];
static const magma_byte_t magmai_S45[256];
static const magma_byte_t magmai_S67[256];

static struct {
    magma_word_t K[32];
} magmai_ctx;

static magma_word_t magmai_subst(magma_word_t X) {
    magma_word_t T = 0;
    T |= magmai_S67[(X >> 24) & 0xFF] << 24;
    T |= magmai_S45[(X >> 16) & 0xFF] << 16;
    T |= magmai_S23[(X >>  8) & 0xFF] <<  8;
    T |= magmai_S01[(X >>  0) & 0xFF] <<  0;
    return T;
}

static void magmai_write_to_pair(magma_word_t* L, magma_word_t* R, const void* src) {
    memcpy(L, (const char*)src + 0, 4);
    memcpy(R, (const char*)src + 4, 4);
#if BLKCPHR_IS_LITTLE
    *L = blkcphr_bswap32(*L);
    *R = blkcphr_bswap32(*R);
#endif
}

static void magmai_read_from_pair(void* dst, magma_word_t L, magma_word_t R) {
    #if BLKCPHR_IS_LITTLE
        L = blkcphr_bswap32(L);
        R = blkcphr_bswap32(R);
    #endif
    memcpy((char*)dst + 0, &L, 4);
    memcpy((char*)dst + 4, &R, 4);
}

int magma_init(const void* key, int bits) {
    int i; const char* K = key;
    if (bits != 256) return 1;

    for (i = 0; i < 8; i++, K += 4) {
        memcpy(magmai_ctx.K +       i , K, 4);
        memcpy(magmai_ctx.K + (31 - i), K, 4);
#if BLKCPHR_IS_LITTLE
        magmai_ctx.K[     i] = blkcphr_bswap32(magmai_ctx.K[     i]);
        magmai_ctx.K[31 - i] = blkcphr_bswap32(magmai_ctx.K[31 - i]);
#endif
    }

    memcpy(magmai_ctx.K +  8, magmai_ctx.K, sizeof(magma_word_t) * 8);
    memcpy(magmai_ctx.K + 16, magmai_ctx.K, sizeof(magma_word_t) * 8);

    return 0;
}

void magma_block_encode(void* dst, const void* src) {
    magma_word_t L, R, T, S, i;
    magmai_write_to_pair(&L, &R, src);

    for (i = 0; i < 32; i++) {
        T = magmai_subst(R + magmai_ctx.K[i]);
        T = T << 11 | T >> 21;
        if (i < 31) {
            S = L; L = R; R = T ^ S;
        } else
            L ^= T;
    }

    magmai_read_from_pair(dst, L, R);
}

void magma_block_decode(void* dst, const void* src) {
    magma_word_t L, R, T, S, i;
    magmai_write_to_pair(&L, &R, src);

    for (i = 32; i --> 0;) {
        T = magmai_subst(R + magmai_ctx.K[i]);
        T = T << 11 | T >> 21;
        if (i > 0) {
            S = L; L = R; R = T ^ S;
        } else
            L ^= T;
    }

    magmai_read_from_pair(dst, L, R);
}

